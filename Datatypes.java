package basic;

public class Datatypes {

	public static void main(String[] args) {
	//boolean example
		boolean ioT=true;
		System.out.println(ioT);
		//byte example byte
		byte range=127;
		System.out.println(range);
		//short example
		short range1;
		range1=127;
		System.out.println(range1);
		//int example
		int range11=300000000;
		System.out.println(range11);
		//long example
		long range3=30000000000L;
		System.out.println(range3);
		//double example double
		double number;
		number=99.9;
		System.out.println(number);
		//float example
		float number1=99.9f;
		System.out.println(number1);
		//char example 
		char letter='\u0041';
		System.out.println(letter);
		char letter2=65;
		System.out.println(letter2);
		char letter3='A';
		System.out.println(letter3);
		
		
	
	}
}
